class Time {
    constructor(e, n) {
        this.minutes = e,
            this.sec = n
    }
}

/* Takes seconds and convers it to a time object with minutes/seconds attributes */
const runTime = (seconds) => {
    let minutes;
    return (minutes = Math.floor(seconds / 60)), new Time(minutes, seconds - 60 * minutes);
}

/* Returns the seconds converted to the format xx:xx ex:(10:30) */
const runTimeString = (seconds) => {
    let timeString;
    let time;
    time = runTime(seconds);
    timeString = time.minutes + ":" + (time.sec < 10 ? '0' + time.sec:time.sec);
    return timeString;
}

/*Takes a time input in format xx:xx and turns it to seconds equivalent*/
function hms(timeString) { 
    let r = 0;
    let s = 1;
    for (let n = timeString.split(":"); n.length > 0; /* No ++ needed because pop() reduces the length of n to 0 */) { 
        if (n[0] == "") {
            n[0] = 0;
        }
        r += s * parseInt(n.pop(), 10), s *= 60; 
    }
    return r 
}

